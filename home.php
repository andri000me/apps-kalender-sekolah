		<h3>Gambaran Umum Sekolah Menengah Atas : </h3>
		<p>
			  &nbsp;&nbsp;&nbsp;&nbsp;
              Sekolah Menengah Atas merupakan lembaga pendidikan. Sekolah ini berdiri berdiri pada tahun .... dengan akreditasi .., beralamat di Jl..... Bapak.... Sebagai kepala sekolah, sekolah ini memiliki dua bidang yaitu Ilmu Pengetahuan Sosial (IPS), dan Ilmu Pengetahuan Alam (IPA). 
              <br/>
              &nbsp;&nbsp;&nbsp;&nbsp;
              Secara garis besar Sekolah Menengah Atas memiliki tugas dan tanggung jawab yaitu sebagai pelaksana pendidikan dalam jangka waktu tertentu sesuai dengan jenis dan sifat sekolah tersebut. Melaksanakan pendidikan dan pengajaran sesuai dengan kewajiban yang berlaku, melaksanakan bimbingan dan penyuluhan bagi siswa-siswi di sekolah, membina Organisasi Siswa Intra Sekolah (OSIS), melaksanakan urusan Tata Usaha (TU), membina kerja sama dengan orang tua siswa,  masyarakat dan instansi terkait, melaksanakan program dinas pendidikan Kota, bertanggung jawab kepada Walikota melalui Kepala Dinas Pendidikan.
