<p>Misi Sekolah Menengah Atas<br>
  Misi Sekolah Menengah Atas diantaranya adalah: <br>
  a. Menciptakan proses belajar mengajar secara efektif, efisien, relevan,  inovatif, aspiratif dan pragmatis. <br>
  b. Siswa mampu belajar efektif dan mandiri sesuai dengan metode yang  disampaikan oleh guru.<br>
  c. Memfasilitasi siswa yang berminat melanjutkan ke perguruan tinggi, baik yang  melalui jalur SPMB maupun PMDK.</p>
<p>Tujuan dari Sekolah Menengah Atas yaitu<br>
  Meningkatkan kualitas dan kuantitas lulusan pada setiap akhir  tahun pelajaran.<br>
  b. Menyiapkan bakal akademis bagi siswa yang ingin melanjutkan ke perguruan  tinggi dan memberikan kecakapan khusus bagi siswa yang tidak melanjutkan  sekolah.<br>
  c. Menyiapkan tim Olah raga, Seni, Sain dan lain-lain untuk mengikuti lomba  tingkat Kota, Provinsi maupun Nasional dengan target memperoleh suara.<br>
  d. Membangun kerjasama dengan komite sekolah dalam pengadaan dan pemeliharaan  sarana prasarana. </p>
